    Scope& currentScope();

    // в”Җв”Җв”Җ Runtime Functions в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ

    void createPanicFunction(llvm::Module* module);
    void createCheckBoundsFunction(llvm::Module* module);

    // в”Җв”Җв”Җ Members в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ

    llvm::LLVMContext& m_context;
    TypeMapping& m_typeMapper;
    StringPool& m_stringPool;
    llvm::IRBuilder<> m_builder;
    std::unique_ptr<llvm::Module> m_module;

    std::vector<Scope> m_scopeStack;
    std::vector<FunctionContext> m_functionStack;
    std::vector<LoopContext> m_loopStack;

    llvm::Function* m_panicFunction = nullptr;
    llvm::Function* m_checkBoundsFunction = nullptr;

    ModuleAST* m_currentModule = nullptr;
    std::string m_moduleName;
